#For loops
print("Printing from 1 to 10 using for loop")
for i in range(1,11):
    print(i)
print()
print("Print all even number from 0 to 10 using for loop")
for i in range(0,11,2):
    print(i)
print()
print("Print all odd number from 1 to 10 using for loop")
for i in range(1,11,2):
    print(i)
print()
print("Printing each in loop")
for each in [1,2,3,4,5]:
    print(each)
print()
for each in "Hello":
    print(each)
print()